                    <p>Saydnaya Military Prison is located 30km north of Damascus, Syria. The prison is under the jurisdiction of the Minister of Defence and operated by the Military Police. Saydnaya became notorious for the use of torture and excessive force following a riot by detainees in 2008. There are two buildings on the Saydnaya site, which between them could contain 10,000-20,000 prisoners.</p>

