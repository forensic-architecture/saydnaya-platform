                    <p>There are no interrogations at Saydnaya. Torture isn’t used to obtain information, but seemingly as a way to degrade, punish and humiliate. Prisoners are targeted relentlessly, unable to “confess” to save themselves from further beatings. Survivors say they dreaded family visits as they were always followed by extensive beatings.</p>

                    <p>Prisoners do not have access to a lawyer. In several cases, relatives were told by the government that the prisoner had died, when in fact they were still being held at Saydnaya.</p>
