                    <p>Detainees are generally transferred to this facility after spending months or even years in detention elsewhere. Such transfers often take place following a flagrantly unfair trial at a secret military court. Others arrive at the prison without having seen a judge and do not know the alleged charges against them or how long they will be detained.</p>

                    <p>Amnesty International spoke to survivors of Saydnaya, who all reported systematic daily beatings, sub-human conditions, degrading treatment, as well as other prisoners dying on a daily basis. Many were denied food and water for prolonged periods. Guards ruthlessly enforced a rule of absolute silence.</p>

                    <blockquote>
