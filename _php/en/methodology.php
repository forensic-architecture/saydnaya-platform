						<p>n April 2016, Amnesty International and Forensic Architecture travelled to Turkey to meet a group of survivors from Saydnaya prison. In recent years, no journalists or monitoring groups which report publicly have been able to visit the prison and speak with prisoners, so this was an opportunity to tell their stories.</p>

						<p>As there are no images of Saydnaya, we were dependent on the memories of survivors to recreate what happened inside. Using architectural and acoustic modelling, we helped witnesses reconstruct the architecture of the prison and their experiences of detention.</p>

						<p>The interview techniques were developed by Forensic Architecture at Goldsmiths, University of London, in consultation with the university’s Forensic Psychology Unit.</p>
