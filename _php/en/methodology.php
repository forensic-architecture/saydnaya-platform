					<h2>1. Pre-interview</h2>

					<p>Before speaking with survivors, Forensic Architecture constructed a 3D model of Saydnaya prison, using written interviews conducted by Amnesty International researchers between December 2015 and February 2016 and satellite images of the building.</p>

					<p>In April 2016, Forensic Architecture and Amnesty International interviewed five former detainees in Istanbul. Each conversation started with an explanation of the way the interview would be conducted and the technology employed. Each witness was told they could stop the process at any given moment.</p>

					<h2>2. Interviews</h2>

					<p>n April 2016, Forensic Architecture and Amnesty International interviewed five former detainees in Istanbul. Each conversation started with an explanation of the way the interview would be conducted and the technology employed. Each witness was told they could stop the process at any given moment.</p>

					<h2>3. 3D modelling</h2>

					<p>The witnesses described the cells and other areas of the prison, including stairwells, corridors, moving doors and windows, to an architect working with 3D modelling software. These new models were then compared to the model of the prison produced before the interviews. Witnesses helped the team to correct errors and refine the architecture of spaces within it.</p>

					<h2>4. Objects</h2>

					<p>We also asked witnesses to choose specific objects they remembered, such as blankets, bowls, furniture and torture tools, from a library we prepared in advance based on previous interviews. If an object wasn’t in the library, we were able to model it. Witnesses then located the objects on the model of the prison, according to their memory of where incidents took place.</p>

					<h2>5. Active participants</h2>

					<p>The witnesses were active participants in the building of the model, and the process sparked new memories of their experiences. As they measured rooms, located windows and doors, and placed objects in the virtual environment, they recalled more fragments of their life in detention.</p>

					<h2>6. Ear-witness testimony</h2>

					<p>Prisoners in Saydnaya developed an acute experience of sound, as what they could see was extremely restricted. There was next to no daylight, and detainees were made to cover their eyes with their hands whenever a guard entered the room. What’s more, speaking was prohibited, so prisoners became attuned to any minute sound. To capture these auditory memories, we developed techniques to solicit “ear-witness testimony” and reconstruct the prison’s architecture through sound:</p>

					<ol type="a">
						<li>Witnesses listened to tones of different decibel levels, and were then asked to match them to the levels of specific incidents inside the prison.</li>
						<li>We used “echo profiling” to determine the size of spaces such as cells, stairwells and corridors. This involved playing different reverberations and asking witnesses to match them with sounds they remembered hearing in the prison.</li>
						<li>We also used “sound artefacts” to simulate prison sounds such as doors, locks and footsteps, which helped to generate further acoustic memories.</li>
					</ol>

					<h2>7. Contradictions</h2>

					<p>We cross-referenced the individual spatial and audio testimonies to construct an overall model of the building. If there were contradictions between different accounts we tried to carefully resolve them. We also made a note of the errors, as they reflect the subjective experience of a detainee and the mental trauma they experienced.</p>
