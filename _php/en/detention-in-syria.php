					<h2>Unimaginable torture</h2>

					<p>Prisoners speak of an endless cycle of beatings. On the journey after arrest. In transit between detention centres. As part of a ‘welcome party’ of abuse on arrival at a prison, used to frighten new arrivals into submission. And then every day for every conceivable minor ‘breaking’ of rules, including talking or not cleaning their cells.</p>

					<p>Many of the people we spoke to said they had been beaten with plastic hose pipes, silicone bars and wooden sticks. Some had been scalded with hot water and burnt with cigarettes. Others were forced to stand in water and given electric shocks.</p>

					<h2>Sub-human conditions</h2>

					<p>People suffer acute mental health problems due to overcrowding and lack of sunlight. In some cases, people told us there could be more than 50 people in a cell as small as 3m by 3m. They sleep in shifts, and have a tiny space of floor where they can sit and eat.</p>

					<p>People die from starvation. From a lack of air in their cells. And from completely treatable diseases. Cuts, ingrown fingernails and rashes become infected, and people die from a desperate lack of medical care.</p>

					<h2>Outside the law</h2>

					<p>In most cases, the Syrian government denies the security forces have even arrested these people. Or they refuse to give any information about their whereabouts. It means that many detainees are “disappeared” – outside the protection of the law – making them especially vulnerable to abuse. </p>

					<p>This is just part of the reality of life in Syria. And it’s part of the reason why so many people are fleeing for neighbouring countries and beyond. More than 11 million Syrians have fled their country since the start of the crisis.</p>

					<h2>Attack on Syrians</h2>

					<p>As the world attempts to resolve the ongoing conflict, the treatment of people in detention must be a priority if Syrians are to have any trust in a lasting peace. Russia has considerable influence over negotiations, in part because of its military involvement in the region. It must ensure that independent monitors are allowed in to fully investigate conditions in these brutal detention centres.</p>
