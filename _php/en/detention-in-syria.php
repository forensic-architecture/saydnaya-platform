						<p>Tens of thousands of people have been tortured and thousands have died in custody in Syria’s prisons since March 2011, the start of the crisis that has forced so many to flee.</p>

						<p>Anyone suspected of opposing the government is at risk. Labourers, business people, students, bloggers, university professors, lawyers, doctors and journalists. People helping their neighbours. Activists standing up for minority groups. Men, women and even children.</p>
