                <h2>End the horror in Syrian prisons</h2>

                <p class="strong">The Syrian government must let in independent monitors to investigate Syria’s brutal detention centres, now.</p>

                <p>Since 2011, thousands of people have died in custody in Syria’s torture prisons. Tens of thousands more have experienced shocking torture. People have been brutally beaten, raped, given electric shocks and more, often to extract forced “confessions”. Anyone suspected of opposing the Syrian government is at risk. Conditions in these brutal detention centres are sub-human. People are dying from starvation. They’re not getting even the most basic health care, and are dying from infected cuts and ingrown fingernails. They suffer acute mental health problems because of the overcrowding and lack of sunlight.</p>

                <p class="strong">Send the message below and tell Russia and the U.S. to use their global influence to ensure that independent monitors are allowed in to investigate conditions in Syria’s torture prisons.</p>

                <p>Dear Co-chairs of the International Syria Support Group,</p>

                <p>I am writing about the ongoing reports by human rights monitoring groups, in particular Amnesty International, that document the widespread use of torture and other ill-treatment and lethal conditions in Syrian detention facilities.</p>

                <p>Since the beginning of the crisis in 2011 the Syrian authorities have subjected tens of thousands of people in Syria to arbitrary detention or enforced disappearance. Many of them have been subjected to torture or other ill-treatment in Syria’s detention centres and thousands are reported to have died in custody as a result.</p>

                <p>These practices clearly violate international law as well as the provisions of UN Security Council resolution 2139 and in many cases constitute war crimes. Insofar as they are carried out as part of a widespread, as well as systematic attack directed against the civilian population, they also amount to crimes against humanity.</p>

                <p>I therefore call on you to use your influence in the International Syria Support Group to urgently ensure that independent detention monitors are allowed to investigate conditions in all detention facilities run by the Syrian government or its security forces. They must also be allowed to speak freely to all people who have been deprived of their liberty.</p>

                <p>I urge you also to use your influence to ensure that the Syrian authorities provide detailed information on all those who are held in their custody, and inform the families of those detained about their legal status and whereabouts.</p>

                <p>Yours sincerely</p>
